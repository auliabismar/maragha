<script lang="ts">
	import { goto } from '$app/navigation';
	import pb from '$lib/pocketbase';

	let email = $state<string>('');
	let password = $state<string>('');
	let passwordConfirm = $state<string>('');
	let error = $state<string | null>(null);
	let loading = $state<boolean>(false);

	async function handleRegister() {
		loading = true;
		error = null;
		try {
			if (password !== passwordConfirm) {
				throw new Error('Passwords do not match.');
			}

			await pb.collection('users').create({
				email,
				password,
				passwordConfirm
			});

			// Optionally, log in the user immediately after registration
			await pb.collection('users').authWithPassword(email, password);

			// Redirect to dashboard or home page on successful registration and login
			goto('/dashboard');
		} catch (err: any) {
			error = err.message || 'An unknown error occurred during registration.';
		} finally {
			loading = false;
		}
	}
</script>

<svelte:head>
	<title>Maragha - Daftar</title>
	<meta name="description" content="Daftar akun baru di Maragha" />
</svelte:head>

<div class="flex items-center justify-center min-h-screen">
	<div class="w-full max-w-md p-8 space-y-6 bg-card rounded-lg shadow-lg">
		<div class="flex justify-center">
			<img src="/src/lib/assets/logo.svg" alt="Maragha Logo" class="h-16 w-16" />
		</div>
		<h2 class="text-3xl font-heading font-bold text-center text-foreground">Daftar</h2>
		<form onsubmit={(e) => { e.preventDefault(); handleRegister(); }} class="space-y-4">
			<div>
				<label for="email" class="block text-sm font-medium text-muted-foreground">Email</label>
				<input
					type="email"
					id="email"
					bind:value={email}
					required
					class="mt-1 block w-full px-3 py-2 border border-border rounded-md shadow-sm focus:outline-none focus:ring-primary focus:border-primary"
				/>
			</div>
			<div>
				<label for="password" class="block text-sm font-medium text-muted-foreground">Password</label>
				<input
					type="password"
					id="password"
					bind:value={password}
					required
					class="mt-1 block w-full px-3 py-2 border border-border rounded-md shadow-sm focus:outline-none focus:ring-primary focus:border-primary"
				/>
			</div>
			<div>
				<label for="passwordConfirm" class="block text-sm font-medium text-muted-foreground"
					>Konfirmasi Password</label
				>
				<input
					type="password"
					id="passwordConfirm"
					bind:value={passwordConfirm}
					required
					class="mt-1 block w-full px-3 py-2 border border-border rounded-md shadow-sm focus:outline-none focus:ring-primary focus:border-primary"
				/>
			</div>

			{#if error}
				<p class="text-sm text-red-500">{error}</p>
			{/if}

			<button
				type="submit"
				disabled={loading}
				class="w-full flex justify-center py-2 px-4 border border-transparent rounded-md shadow-sm text-sm font-medium text-primary-foreground bg-primary hover:bg-primary-dark focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-primary disabled:opacity-50"
			>
				{#if loading}
					Memuat...
				{:else}
					Daftar
				{/if}
			</button>
		</form>
		<p class="text-center text-sm text-muted-foreground">
			Sudah punya akun? <a href="/login" class="font-medium text-primary hover:text-accent">Masuk</a>
		</p>
	</div>
</div>