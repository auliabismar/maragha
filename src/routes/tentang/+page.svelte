      memastikan bahwa karya-karya berharga ini tidak hilang oleh waktu dan dapat diakses
      oleh siapa saja, di mana saja.
    </p>

    <h3 class="mb-4 mt-10 font-heading text-2xl font-semibold text-[var(--foreground)]">
      3. Menyemarakkan Warisan (Bukan Sekadar Menyalin)
    </h3>
    <p class="mb-4">
      Tujuan kami bukan hanya melestarikan, tetapi juga "menyemarakkan". Para cendekiawan
