    <h2 class="mb-6 font-heading text-3xl font-semibold text-[var(--foreground)]">
      Mengapa "Maragha"?
    </h2>
    <p class="mb-4">
      Nama kami terinspirasi dari <strong>Observatorium Maragha</strong> (berdiri 1259 M),
      sebuah institusi yang melambangkan puncak pencapaian ilmiah dan semangat kolaborasi
      dalam sejarah peradaban Islam. Bagi kami, Maragha bukan sekadar nama; ia adalah sebuah
      filosofi.
    </p>
