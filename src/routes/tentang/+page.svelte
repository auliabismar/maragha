      revolusi—sebuah "institusi ilmiah paling maju di Eurasia" pada masanya dan sebuah "pusat
      penelitian ilmiah skala besar pertama yang didanai negara dan bersifat jangka panjang".
    </p>

    <h3 class="mb-4 mt-10 font-heading text-2xl font-semibold text-[var(--foreground)]">
      1. Pusat Kolaborasi Global (Gotong Royong)
    </h3>
    <p class="mb-4">
      Misi utama kami adalah "gotong royong literasi". Ini adalah cerminan langsung dari
      Observatorium Maragha. Maragha bukanlah karya satu orang, melainkan sebuah "kongregasi
      para pemikir" (<em>congregation of minds</em>).
    </p>
    <p class="mb-6">
      Nasir al-Din al-Tusi secara aktif merekrut talenta-talenta terbaik dari seluruh
      penjuru dunia, termasuk para insinyur dari Damaskus seperti Mu'ayyad al-Din al-'Urdi,
      dan bahkan astronom dari Tiongkok seperti Fao Munji. Para cendekiawan dari berbagai
      latar belakang ini berkumpul, berdebat, dan bekerja sama untuk satu tujuan: memetakan
      kosmos. Semangat kolaborasi lintas batas inilah yang ingin kami hidupkan kembali dalam
      misi penerjemahan digital.
    </p>

    <h3 class="mb-4 mt-10 font-heading text-2xl font-semibold text-[var(--foreground)]">
      2. Merawat Khazanah Pengetahuan (Perpustakaan Digital)
    </h3>
    <p class="mb-4">
      Maragha bukan hanya observatorium; ia adalah sebuah kompleks perpustakaan yang
